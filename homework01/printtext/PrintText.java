package homework01.printtext;

public class PrintText {
    public static void main(String[] args) { // the main() method - it's required, so you'll see it in every Java program
        // every block of code must be within curly braces {} and every statement must end with a semicolon ;

        // to print a line of text to the screen, we can use println() method
        System.out.println("Hello World"); // note that we must put the text inside the quotation marks ""

        // we could also use print() method, but unlike println(), it doesn't insert a new line at the end of the output
        System.out.print("Hello");
        System.out.print("World");

    }
}
